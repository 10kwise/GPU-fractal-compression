def download_kodak(dest='datasets/kodak'):
    """Download Kodak-24 dataset (24 images, 768×512, lossless PNG)."""
    import requests
    os.makedirs(dest, exist_ok=True)
    base = 'http://r0k.us/graphics/kodak/kodak/'
    downloaded = []
    for i in range(1, 25):
        fname = f'kodim{i:02d}.png'
        fpath = os.path.join(dest, fname)
        if os.path.exists(fpath):
            downloaded.append(fpath)
            continue
        url = base + fname
        try:
            r = requests.get(url, timeout=30)
            if r.status_code == 200:
                with open(fpath, 'wb') as f:
                    f.write(r.content)
                downloaded.append(fpath)
                print(f'  Downloaded {fname}')
        except Exception as e:
            print(f'  FAIL {fname}: {e}')
    print(f'Kodak: {len(downloaded)}/24 images')
    return sorted(downloaded)


def download_bsd500(dest='datasets/bsd500'):
    """Download BSD500 test images (200 images)."""
    import requests, tarfile
    os.makedirs(dest, exist_ok=True)
    existing = sorted([
        os.path.join(dest, f) for f in os.listdir(dest)
        if f.endswith('.jpg') or f.endswith('.png')
    ])
    if len(existing) >= 100:
        print(f'BSD500: {len(existing)} images already present')
        return existing

    url = 'https://www2.eecs.berkeley.edu/Research/Projects/CS/vision/grouping/BSR/BSR_bsds500.tgz'
    print('Downloading BSD500 archive (~70MB)...')
    try:
        r = requests.get(url, timeout=180, stream=True)
        if r.status_code == 200:
            buf = io.BytesIO(r.content)
            with tarfile.open(fileobj=buf, mode='r:gz') as tar:
                # Extract test + train + val images
                imgs = [m for m in tar.getmembers()
                        if '/images/' in m.name and
                        (m.name.endswith('.jpg') or m.name.endswith('.png'))]
                for m in imgs:
                    fname = os.path.basename(m.name)
                    fpath = os.path.join(dest, fname)
                    f = tar.extractfile(m)
                    if f:
                        with open(fpath, 'wb') as out:
                            out.write(f.read())
            result = sorted([
                os.path.join(dest, f) for f in os.listdir(dest)
                if f.endswith('.jpg') or f.endswith('.png')
            ])
            print(f'  BSD500: extracted {len(result)} images')
            return result
        else:
            print(f'  BSD500 download failed: HTTP {r.status_code}')
            return []
    except Exception as e:
        print(f'  BSD500 download error: {e}')
        return []


def download_div2k(dest='datasets/div2k', split='train'):
    """
    Download DIV2K training set (800 images, ~2K resolution).
    Standard dataset for super-resolution research.
    """
    import requests, zipfile
    os.makedirs(dest, exist_ok=True)
    existing = sorted([
        os.path.join(dest, f) for f in os.listdir(dest)
        if f.endswith('.png')
    ])
    if len(existing) >= 400:
        print(f'DIV2K: {len(existing)} images already present')
        return existing

    url = 'http://data.vision.ee.ethz.ch/cvl/DIV2K/DIV2K_train_HR.zip'
    print('Downloading DIV2K training set (~3.3GB — this takes a few minutes)...')
    try:
        r = requests.get(url, timeout=600, stream=True)
        if r.status_code == 200:
            zip_path = os.path.join(dest, '_div2k.zip')
            with open(zip_path, 'wb') as f:
                for chunk in r.iter_content(chunk_size=8192*16):
                    f.write(chunk)
            with zipfile.ZipFile(zip_path, 'r') as z:
                for member in z.namelist():
                    if member.endswith('.png'):
                        fname = os.path.basename(member)
                        if fname:
                            with z.open(member) as src, \
                                 open(os.path.join(dest, fname), 'wb') as dst:
                                dst.write(src.read())
            os.remove(zip_path)
            result = sorted([
                os.path.join(dest, f) for f in os.listdir(dest)
                if f.endswith('.png')
            ])
            print(f'  DIV2K: extracted {len(result)} images')
            return result
        else:
            print(f'  DIV2K download failed: HTTP {r.status_code}')
            return []
    except Exception as e:
        print(f'  DIV2K download error: {e}')
        return []


