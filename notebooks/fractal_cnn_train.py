class SSIMLoss(nn.Module):
    """Differentiable SSIM loss (1 - SSIM) for single-channel images."""
    def __init__(self, window_size=11, sigma=1.5):
        super().__init__()
        self.window_size = window_size
        # Create Gaussian window
        coords = torch.arange(window_size, dtype=torch.float32) - window_size // 2
        g = torch.exp(-(coords ** 2) / (2 * sigma ** 2))
        g = g / g.sum()
        window = g.unsqueeze(1) @ g.unsqueeze(0)  # 2D Gaussian
        self.register_buffer('window', window.unsqueeze(0).unsqueeze(0))  # (1,1,H,W)

    def forward(self, pred, target):
        C1 = 0.01 ** 2
        C2 = 0.03 ** 2

        mu1 = torch.nn.functional.conv2d(pred, self.window, padding=self.window_size//2)
        mu2 = torch.nn.functional.conv2d(target, self.window, padding=self.window_size//2)

        mu1_sq  = mu1 ** 2
        mu2_sq  = mu2 ** 2
        mu1_mu2 = mu1 * mu2

        sigma1_sq = torch.nn.functional.conv2d(
            pred * pred, self.window, padding=self.window_size//2) - mu1_sq
        sigma2_sq = torch.nn.functional.conv2d(
            target * target, self.window, padding=self.window_size//2) - mu2_sq
        sigma12 = torch.nn.functional.conv2d(
            pred * target, self.window, padding=self.window_size//2) - mu1_mu2

        ssim = ((2 * mu1_mu2 + C1) * (2 * sigma12 + C2)) / \
               ((mu1_sq + mu2_sq + C1) * (sigma1_sq + sigma2_sq + C2))

        return 1.0 - ssim.mean()


class VGGPerceptualLoss(nn.Module):
    """
    Perceptual loss using VGG16 conv3_3 features.
    Captures texture and structural similarity beyond pixel-level MSE.
    """
    def __init__(self):
        super().__init__()
        from torchvision import models
        vgg = models.vgg16(weights=models.VGG16_Weights.DEFAULT)
        # Use features up to conv3_3 (layer index 16)
        self.features = nn.Sequential(*list(vgg.features[:16]))
        for p in self.features.parameters():
            p.requires_grad = False
        # VGG normalisation
        self.register_buffer('mean', torch.tensor([0.485, 0.456, 0.406]).view(1,3,1,1))
        self.register_buffer('std', torch.tensor([0.229, 0.224, 0.225]).view(1,3,1,1))

    def forward(self, pred, target):
        # Expand single-channel to 3-channel for VGG
        pred_3   = pred.expand(-1, 3, -1, -1)
        target_3 = target.expand(-1, 3, -1, -1)
        # Normalise to VGG input range
        pred_n   = (pred_3 - self.mean) / self.std
        target_n = (target_3 - self.mean) / self.std
        # Feature distance
        return torch.nn.functional.mse_loss(
            self.features(pred_n), self.features(target_n))


class CompositeLoss(nn.Module):
    """
    Combined loss: α·MSE + β·(1-SSIM) + γ·VGG_perceptual

    MSE drives PSNR directly.
    SSIM captures structural/edge quality.
    VGG perceptual captures texture quality.
    """
    def __init__(self, alpha=1.0, beta=0.1, gamma=0.01, device='cuda'):
        super().__init__()
        self.alpha = alpha
        self.beta  = beta
        self.gamma = gamma
        self.mse_loss  = nn.MSELoss()
        self.ssim_loss = SSIMLoss().to(device)
        self.vgg_loss  = VGGPerceptualLoss().to(device)

    def forward(self, pred, target):
        l_mse  = self.mse_loss(pred, target)
        l_ssim = self.ssim_loss(pred, target)
        l_vgg  = self.vgg_loss(pred, target)
        return self.alpha * l_mse + self.beta * l_ssim + self.gamma * l_vgg


