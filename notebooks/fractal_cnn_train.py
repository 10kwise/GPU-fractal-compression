def generate_training_pair(image_path, time_budget=60):
    """
    Run fractal encode/decode on one image, return (decoded_Y, original_Y).
    Both are 2D uint8 arrays of the Y (luminance) channel.
    """
    try:
        img_raw = np.array(Image.open(image_path).convert('RGB'))

        # Skip very small images
        if img_raw.shape[0] < 64 or img_raw.shape[1] < 64:
            return None

        # Limit large images to keep training time reasonable
        max_dim = 768
        h, w = img_raw.shape[:2]
        if max(h, w) > max_dim:
            scale = max_dim / max(h, w)
            new_h, new_w = int(h * scale), int(w * scale)
            img_raw = np.array(Image.fromarray(img_raw).resize(
                (new_w, new_h), Image.LANCZOS))

        img, orig_H, orig_W = pad_to_block_multiple(img_raw)
        H_pad, W_pad = img.shape[:2]

        ycbcr = rgb_to_ycbcr(img)
        original_y = ycbcr[:orig_H, :orig_W, 0]

        cfg = analyse_image(ycbcr[:,:,0], time_budget)
        y_tf, _ = encode_channel_gpu(ycbcr[:,:,0], cfg)
        decoded_y = decode_fractal(y_tf, (H_pad, W_pad), N_ITERATIONS)
        decoded_y = decoded_y[:orig_H, :orig_W]

        return decoded_y, original_y

    except Exception as e:
        print(f'  ERROR on {os.path.basename(image_path)}: {e}')
        return None


def extract_patches(decoded_y, original_y, patch_size=64, stride=32):
    """Extract overlapping patches from a (decoded, original) Y channel pair."""
    H, W = decoded_y.shape
    patches_in, patches_out = [], []

    for y in range(0, H - patch_size + 1, stride):
        for x in range(0, W - patch_size + 1, stride):
            p_in  = decoded_y[y:y+patch_size, x:x+patch_size].astype(np.float32) / 255.0
            p_out = original_y[y:y+patch_size, x:x+patch_size].astype(np.float32) / 255.0
            patches_in.append(p_in)
            patches_out.append(p_out)

            # Data augmentation: horizontal flip
            patches_in.append(np.fliplr(p_in).copy())
            patches_out.append(np.fliplr(p_out).copy())

    return patches_in, patches_out


def generate_all_pairs(image_paths, output_dir='training_pairs',
                       patch_size=64, stride=32, max_images=None):
    """
    Generate training pairs from all images. Saves patches as .npz files.
    Returns paths to saved files.
    """
    os.makedirs(output_dir, exist_ok=True)

    if max_images:
        image_paths = image_paths[:max_images]

    all_inputs, all_targets = [], []
    n = len(image_paths)

    for i, path in enumerate(image_paths):
        print(f'[{i+1}/{n}] Processing {os.path.basename(path)}...')
        result = generate_training_pair(path)
        if result is None:
            continue

        decoded_y, original_y = result
        p_in, p_out = extract_patches(decoded_y, original_y, patch_size, stride)
        all_inputs.extend(p_in)
        all_targets.extend(p_out)
        print(f'  → {len(p_in)} patches (total: {len(all_inputs)})')

        # Save incrementally every 50 images
        if (i + 1) % 50 == 0 or (i + 1) == n:
            save_path = os.path.join(output_dir, f'pairs_batch_{i+1}.npz')
            np.savez_compressed(save_path,
                                inputs=np.array(all_inputs),
                                targets=np.array(all_targets))
            print(f'  Saved checkpoint: {save_path} ({len(all_inputs)} patches)')

    # Final save
    final_path = os.path.join(output_dir, 'all_pairs.npz')
    np.savez_compressed(final_path,
                        inputs=np.array(all_inputs),
                        targets=np.array(all_targets))
    print(f'\n=== Generated {len(all_inputs)} training patches ===')
    print(f'Saved to {final_path}')
    return final_path


