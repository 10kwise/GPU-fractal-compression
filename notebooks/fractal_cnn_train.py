class FractalPatchDataset(Dataset):
    """Dataset of (decoded_Y_patch, original_Y_patch) pairs."""

    def __init__(self, npz_path):
        data = np.load(npz_path)
        self.inputs  = data['inputs']   # (N, H, W) float32 in [0, 1]
        self.targets = data['targets']  # (N, H, W) float32 in [0, 1]
        print(f'Loaded {len(self.inputs)} patches from {npz_path}')

    def __len__(self):
        return len(self.inputs)

    def __getitem__(self, idx):
        x = torch.from_numpy(self.inputs[idx]).unsqueeze(0)   # (1, H, W)
        y = torch.from_numpy(self.targets[idx]).unsqueeze(0)  # (1, H, W)
        return x, y


