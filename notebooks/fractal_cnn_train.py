# SECTION 7: Training Loop
# ═══════════════════════════════════════════════════════════════════════════════

def train_cnn(pairs_path, output_path='fractal_cnn_weights.pt',
              epochs=80, batch_size=32, lr=1e-3, val_split=0.1,
              device=None):
    """
    Train the EdgeAwareResidualCNN on fractal decode training pairs.

    Args:
        pairs_path:  Path to .npz file with 'inputs' and 'targets' arrays
        output_path: Where to save the trained weights
        epochs:      Number of training epochs
        batch_size:  Batch size for training
        lr:          Initial learning rate
        val_split:   Fraction of data used for validation
        device:      'cuda' or 'cpu' (auto-detected if None)
    """
    if device is None:
        device = 'cuda' if torch.cuda.is_available() else 'cpu'
    print(f'Training on {device}')

    # Load dataset
    dataset = FractalPatchDataset(pairs_path)
    n_val   = int(len(dataset) * val_split)
    n_train = len(dataset) - n_val
    train_ds, val_ds = torch.utils.data.random_split(
        dataset, [n_train, n_val],
        generator=torch.Generator().manual_seed(42))

    train_loader = DataLoader(train_ds, batch_size=batch_size, shuffle=True,
                              num_workers=2, pin_memory=True)
    val_loader   = DataLoader(val_ds, batch_size=batch_size, shuffle=False,
                              num_workers=2, pin_memory=True)

    print(f'Train: {n_train} patches, Val: {n_val} patches')

    # Model
    model = EdgeAwareResidualCNN(n_channels=64, n_layers=4).to(device)
    n_params = sum(p.numel() for p in model.parameters())
    print(f'Model: {n_params:,} parameters ({n_params*4/1024:.1f} KB)')

    # Loss and optimizer
    criterion = CompositeLoss(alpha=1.0, beta=0.1, gamma=0.01, device=device)
    optimizer = optim.Adam(model.parameters(), lr=lr)
    scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs)

    best_val_loss = float('inf')
    best_val_psnr = 0.0
    history = []

    for epoch in range(epochs):
        # ── Train ──────────────────────────────────────────────────────
        model.train()
        train_loss = 0.0
        for batch_x, batch_y in train_loader:
            batch_x, batch_y = batch_x.to(device), batch_y.to(device)
            pred = model(batch_x)
            loss = criterion(pred, batch_y)
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()
            train_loss += loss.item() * batch_x.size(0)
        train_loss /= n_train

        # ── Validate ───────────────────────────────────────────────────
        model.eval()
        val_loss = 0.0
        val_mse  = 0.0
        with torch.no_grad():
            for batch_x, batch_y in val_loader:
                batch_x, batch_y = batch_x.to(device), batch_y.to(device)
                pred = model(batch_x)
                val_loss += criterion(pred, batch_y).item() * batch_x.size(0)
                val_mse  += torch.nn.functional.mse_loss(pred, batch_y).item() * batch_x.size(0)
        val_loss /= n_val
        val_mse  /= n_val
        val_psnr = 10 * math.log10(1.0 / val_mse) if val_mse > 0 else 100.0

        scheduler.step()

        history.append({
            'epoch': epoch + 1,
            'train_loss': round(train_loss, 6),
            'val_loss': round(val_loss, 6),
            'val_psnr': round(val_psnr, 2),
            'lr': optimizer.param_groups[0]['lr'],
        })

        if (epoch + 1) % 5 == 0 or epoch == 0:
            print(f'  Epoch {epoch+1:3d}/{epochs}  '
                  f'train={train_loss:.6f}  val={val_loss:.6f}  '
                  f'PSNR={val_psnr:.2f} dB  lr={optimizer.param_groups[0]["lr"]:.1e}')

        # Save best model
        if val_loss < best_val_loss:
            best_val_loss = val_loss
            best_val_psnr = val_psnr
            torch.save(model.state_dict(), output_path)

    # Final report
    model_size = os.path.getsize(output_path) / 1024
    print(f'\n{"="*50}')
    print(f'  Training complete')
    print(f'  Best val PSNR: {best_val_psnr:.2f} dB')
    print(f'  Model saved:   {output_path} ({model_size:.1f} KB)')
    print(f'{"="*50}')

    # Save training history
    hist_path = output_path.replace('.pt', '_history.json')
    with open(hist_path, 'w') as f:
        json.dump(history, f, indent=2)
    print(f'  History:       {hist_path}')

    return model, history


